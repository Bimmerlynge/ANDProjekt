package com.bimmerlynge.andprojekt.ui.home;

public class Group {
    private String name;
    private double remain;

    public Group(String name, double remain) {
        this.name = name;
        this.remain = remain;
    }

    public String getName() {
        return name;
    }

    public double getRemain() {
        return remain;
    }

}


